"""
Pre-training dataset generation for DynamicCausalPFN.

After pre-training on these datasets, 
we will evaluate DynamicCausalPFN's performance on mimic test dataset - zeroshot, fewshot, fine-tuning

Therefore, a large-scale of pre-training dataset is generated
baesd on DAG structure following mimic data
3-dimensional vitals (Time-varying covariate),
1-dimensional static_feature (age),
1-dimensional treatment,
1-dimensional outcome.

sequence_length = 30
projection_horizon = 2 (tau = 2 + 1 = 3)

At each timestep,
1) treatment is generated by treatment network given current covariates (vitals + prev_outcome).
2) outcome is generated by outcome network given covariates (vitals + prev_outcome), treatment, static feature.

current outcome is then put into the next timestep's covariate.

And again generate treatment, outcome, .. and so on. 

Iterate this procedure to make timeseries of length = 30.

Given network parameters, noises, etc, a dataset is filled with M timeseries samples.

Do this for different parameters, and a large-scale dataset is generated. (N datasets).


We generate synthetic datasets (tasks) as follows. 
Each dataset is produced by:
- Sample ONE treatment network and ONE outcome network (fixed within the dataset).
- Generating M trajectories (samples), each of length T=seq_length.

DAG at each time step t:
	X_t -> A_t
	X_t -> Y_t
	A_t -> Y_t
	S    -> Y_t

Dynamics:
	vitals: 3-dimensional length=30 timeseries is sampled 
	initial outcome sampled from uniform distribution  
	X_0[0:3] <- vitals_0, X_0[3] <- initial outcome
	for t=0, ...T-2:
		A_t ~ Bernoulli( sigmoid ( f_A(X_t) ) )
		Y_t(a) = f_Y([X_t, a, S]) for a in {0, 1}
		Y_t = Y_t(A_t)
		X_{t+1}[3] <- Y_t
		X_{t+1}[0:3] <- vitals_{t+1}
	
Outputs are aligned with mimic_iii.semi_synthetic_dataset.py keys
	outcomes, static_features, vitals, treatments, sequence_lengths
	"""

