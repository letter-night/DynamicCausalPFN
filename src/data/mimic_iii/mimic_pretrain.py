def generate_pretraining_dataset(
		num_samples: int,
		seq_length: int,
		*,
		seed: Optional[int] = None,
		treatment_assigner: Optional[TreatmentAssigner] = None,
		outcome_generator: Optional[OutcomeGenerator] = None,
		vital_generator: Optional[VitalGenerator] = None,
		initial_outcome_sampler: Optional[Callable[[int], np.ndarray]] = None,
		static_feature_sampler: Optional[Callable[[int], np.ndarray]] = None,
) -> Dict[str, np.ndarray]:
	"""
	Generate ONE synthetic dataset (one task) with num_samples trajectories.
	
	- A single treatment network and outcome network is sampled once and reused.
	- num_samples of len=seq_length timeseries (vitals) is sampled.
		Each patient trajectory gets it own sampled vital time series.
	- At each t:
		Covariate X_t is composed with vitals_t and previous outcome Y_{t-1}
		A_t sampled from treatment net given current covariate X_t 
		Y_t sampled from outcome net given X_t, A_t, S
	
	Returns dict compatible with MIMIC semi-synthetic interface:
		outcomes, static_features, vitals, treatments, sequence_lengths.
	"""

	if seed is not None:
		np.random.seed(seed)
	
	# Sample patient-level static features and initial outcomes
	if initial_outcome_sampler is None:
		initial_outcomes = _sample_initial_outcomes(num_samples)
	else:
		initial_outcomes = initial_outcome_sampler(num_samples)
	
	if static_feature_sampler is None:
		static_features = np.random.choice(np.arange(10, 90), size=num_samples)
	else:
		static_features = static_feature_sampler(num_samples)
	
	# Static feature tensor S (N, 1).
	S = static_features.reshape(-1, 1)

	# Create / sample mechanisms (fixed within this dataset)
	if treatment_assigner is None:
		treatment_assigner = TreatmentAssigner()
	if outcome_generator is None:
		outcome_generator = OutcomeGenerator()
	
	# Create num_patients vitals with length=seq_length.
	# Each vitals is used for each patient trajectory.
	if vital_generator is None:
		vital_generator = VitalGenerator(num_patients=num_samples, seq_length=seq_length)
	
	# vitals: [N, T, 3]
	vitals = np.asarray(vital_generator.generate_many_vitals()) #[num_samples, vital_dim, seq_length]
	vitals = np.transpose(vitals, (0, 2, 1)) # [num_samples, seq_length, vital_dim(3)]

	# Raw outcome trajectory: [N, T]
	outcomes = np.zeros((num_samples, seq_length))

	# Raw treatment trajectory: [N, T, 1]
	# treatments[:, 0] stays zero, because row 0 has no previous treatment
	treatments = np.zeros((num_samples, seq_length, 1))

	# Number of valid one-step transitions
	sequence_lengths = np.full((num_samples, ), seq_length - 1)

	# Initial outcome at time 0
	outcomes[:, 0] = initial_outcomes

	# X_t = [vitals_t(3), previous_outcome(1)] => dim 4
	treatment_assigner.sample_new_mechanism(input_dim=4)
	outcome_generator.sample_new_mechanism(input_dim=4 + 1 + S.shape[1])
	
	# vetorized over patients
	for t in range(seq_length - 1):
		x_t = np.concatenate([vitals[:, t, :], outcomes[:, t][:, None]], axis=1) # (N, 4)

		a_t = treatment_assigner.generate_treatments(x_t, resample=False) # (N,)
		treatments[:, t+1, 0] = a_t 

		y_next, _, _ = outcome_generator.generate_outcomes(x_t, a_t, S, resample=False)
		outcomes[:, t+1] = y_next 

	outputs = {
		"outcomes": outcomes, # (N, T)
		"treatments": treatments, # (N, T, 1)
		"vitals":vitals, # (N, T, 3)
		"sequence_lengths": sequence_lengths, # (N,)
		"static_features": static_features, # (N, ) or (N, 1) 
	}

	return outputs


def get_pretrain_scaling_params(sim: Dict[str, np.ndarray]):
	"""
	Compute per-feature normalisation statistics for:
		- outcomes
		- vitals
		- static_features
	
	Uses only active transition steps for outcomes and vitals.
	"""

	outcomes = np.asarray(sim['outcomes'])
	vitals = np.asarray(sim['vitals'])
	static_features = np.asarray(sim['static_features'])
	sequence_lengths = np.asarray(sim['sequence_lengths']).astype(int)

	if outcomes.ndim == 2:
		outcomes = outcomes[:, :, None]
	
	if vitals.ndim == 2:
		vitals = vitals[:, :, None]
	
	if static_features.ndim == 1:
		static_features = static_features[:, None]
	
	n, t_out, d_out = outcomes.shape
	_, t_vit, d_vit = vitals.shape

	active_mask = np.zeros((n, t_out - 1), dtype=bool)
	for i in range(n):
		active_len = min(int(sequence_lengths[i]), t_out - 1)
		active_mask[i, :active_len] = True
	
	active_outputs = outcomes[:, 1:, :][active_mask]
	active_vitals = vitals[:, 1:, :][active_mask]

	if active_outputs.ndim == 1:
		active_outputs = active_outputs[:, None]
	
	if active_vitals.ndim == 1:
		active_vitals = active_vitals[:, None]
	
	output_means = active_outputs.mean(axis=0)
	output_stds = active_outputs.std(axis=0)
	output_stds = np.where(output_stds ==0, 1.0, output_stds)

	vitals_means = active_vitals.mean(axis=0)
	vitals_stds = active_vitals.std(axis=0)
	vitals_stds = np.where(vitals_stds==0, 1.0, vitals_stds)

	static_features_means = static_features.mean(axis=0)
	static_features_stds = static_features.std(axis=0)
	static_features_stds = np.where(static_features_stds==0, 1.0, static_features_stds)

	return {
		'output_means':output_means,
		'output_stds':output_stds,
		'vitals_means':vitals_means,
		'vitals_stds':vitals_stds,
		'static_features_means':static_features_means,
		'static_features_stds':static_features_stds,
	}
	

# Generate N datasets (tasks)
def generate_many_pretraining_datasets(
		num_datasets: int,
		samples_per_dataset: int,
		seq_length: int,
		*, 
		base_seed: Optional[int] = None,
		**kwargs,
) -> list[Dict[str, np.ndarray]]:
	"""
	Returns a list of datasets [D_0, ..., D_{N-1}], each generated with a different mechanism.
	Useful if PFN training samples "a dataset/task" per batch element.
	"""
	out = []
	for i in range(num_datasets):
		seed_i = None if base_seed is None else base_seed + i
		ds = generate_pretraining_dataset(
			samples_per_dataset, 
			seq_length, 
			seed=seed_i, 
			**kwargs,
		)
		out.append(ds)
	return out 



